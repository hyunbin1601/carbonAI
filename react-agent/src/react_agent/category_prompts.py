def get_category_prompt(base_prompt: str, category: str, agent_type: str = "student") -> str:
    """카테고리별 프롬프트를 base_prompt에 추가하여 반환

    Args:
        base_prompt: 기본 에이전트 프롬프트 (STUDENT_PROMPT 또는 TEACHER_PROMPT)
        category: 카테고리 이름 ("탄소배출권", "규제대응", "고객상담")
        agent_type: 에이전트 유형 ("student" 또는 "teacher")

    Returns:
        카테고리 프롬프트가 추가된 전체 프롬프트
    """
    # agent_type에 따라 적절한 프롬프트 선택
    if agent_type == "teacher":
        category_prompt = TEACHER_CATEGORY_PROMPTS.get(category, "")
    else:  # student
        category_prompt = STUDENT_CATEGORY_PROMPTS.get(category, "")

    if category_prompt:
        return base_prompt + "\n\n" + category_prompt
    return base_prompt
